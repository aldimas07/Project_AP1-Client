    public void initTableCheckout() {
        for (Menu a : listMenuSelected) {
            System.out.println(a.getIdMenu());
        }
        System.out.println("asdasdasdasdasdasd");
        Iterator it = daftarPilih.values().iterator();
        while (it.hasNext()) {
            Menu mnu = (Menu) it.next();
            System.out.println(mnu.getIdMenu());
        }
        DefaultTableModel dTblSel = (DefaultTableModel) tblMenuCheckout.getModel();
        dTblSel.setRowCount(0);
        int harga_total = 0;
        for (Menu menu : this.listMenuSelected) {
            Object[] objMenu = new Object[4];
            objMenu[0] = menu.getIdMenu();
            objMenu[1] = menu.getNamaMenu();
            objMenu[2] = menu.getJmlhPesan();
            objMenu[3] = (menu.getJmlhPesan() * menu.getHarga());
            harga_total = harga_total + Integer.parseInt(objMenu[3].toString());
            dTblSel.addRow(objMenu);
        }
        totalPesan = harga_total;
        jLabel3.setText("Rp " + harga_total);
    }
